package com.example.cashedi.models;

public class LandlinePhone {
    private String prefix; // Préfixe
    private String number; // Numéro

    // Getters et Setters
    public String getPrefix() {
        return prefix;
    }

    public void setPrefix(String prefix) {
        this.prefix = prefix;
    }

    public String getNumber() {
        return number;
    }

    public void setNumber(String number) {
        this.number = number;
    }
}

